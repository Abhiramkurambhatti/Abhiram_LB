// Program to display 5 times Hello on screen 

#include<stdio.h>



void Display()				// Defination
{
	int iCnt =0 ;

	//1 Initialization
	//2 Condition
	//3 Displacement
	//4 Loop body

	for(iCnt=1; iCnt<=5; iCnt++)
	{
		printf("Hello\n");
	}
}

int main()					
{
	Display();				
	
	return 0;
}